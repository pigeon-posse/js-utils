#!/bin/bash

###############################################################################
# EXECUTE OF SOURCE IN FOLDER 'EXECS'
###############################################################################

SCRIPT_DIR=$( cd -- "$( dirname -- "${BASH_SOURCE[0]}" )" &> /dev/null && pwd )

FILE=$1

if [ -n "${FILE}" ]; then 

	sh ${SCRIPT_DIR}/execs/${FILE}.sh "$@"

fi


###############################################################################